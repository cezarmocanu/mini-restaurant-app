  static List<Map<String, dynamic>> generateOrdersMockData(int ordersCount, List<Map<String, dynamic>> tables) {
    List<Map<String, dynamic>> orders = [];
    Random random = Random();

    List<String> statuses = [OrderStatus.newOrder, OrderStatus.inProgress, OrderStatus.done];

    for (int i = 0; i < ordersCount; i++) {
      Map<String, dynamic>? order;
      String status = statuses[random.nextInt(statuses.length)];

      if (status == OrderStatus.newOrder || status == OrderStatus.inProgress) {
        order = {
          "id": i,
          "idTable": tables[random.nextInt(tables.length)]["id"],
          "status": status,
          "createdTime": DateTime.now().subtract(Duration(minutes: random.nextInt(15))).millisecondsSinceEpoch,
          "preparedTime": null,
        };
      } else if (status == OrderStatus.done) {
        order = {
          "id": i,
          "idTable": tables[random.nextInt(tables.length)]["id"],
          "status": status,
          "createdTime": DateTime.now().subtract(Duration(minutes: random.nextInt(15))).millisecondsSinceEpoch,
          "preparedTime": DateTime.now().add(Duration(minutes: random.nextInt(15))).millisecondsSinceEpoch,
        };
      }

      if (order != null) {
        orders.add(order);
      }
    }

    return orders;
  }

